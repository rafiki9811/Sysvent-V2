    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int filaSeleccionadaEmpleado = tablaEmpleado.getSelectedRow();
        if(filaSeleccionadaEmpleado>=0){
            jButton5.setEnabled(true);
            jButton5.setVisible(true);
            jButton4.setVisible(false);
            jButton9.setVisible(false);
            jButton2.setVisible(true);
            jComboBox1.setEnabled(false);
            habilitarText();
            idBuscar=idEmpleado;
            jTextNombre.setText(tablaEmpleado.getValueAt(filaSeleccionadaEmpleado,0).toString());
            jTextApePa.setText(tablaEmpleado.getValueAt(filaSeleccionadaEmpleado,1).toString());
            jTextApeMa.setText(tablaEmpleado.getValueAt(filaSeleccionadaEmpleado,2).toString());
            jTextArea1.setText(tablaEmpleado.getValueAt(filaSeleccionadaEmpleado,3).toString());
            jTextCel.setText(tablaEmpleado.getValueAt(filaSeleccionadaEmpleado,4).toString());
            jTextUser6.setEnabled(false);
            jPasswordField1.setEnabled(false);
        } else{
            JOptionPane.showMessageDialog(null, "No haz seleccionado ningun elemento");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

